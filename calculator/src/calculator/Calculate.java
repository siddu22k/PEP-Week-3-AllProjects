package calculator;

//Class consisting all the methods for calculations
public class Calculate {
	
	//add method for the addition of Two numbers
	public int add(int x,int y)
	{
		return x+y;
	}
	
	//multiply method for the multiplication of two numbers
	public int multiply(int x, int y) {
		return x*y;
	}
	
	//divide method for the division of two numbers
	public int divide(int x, int y) {
		return x/y;
	}

}
